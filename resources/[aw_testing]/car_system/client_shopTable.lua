--	==========     Получение цены из таблицы автосалонов     ==========
function getCarPrice(veh)
	if isElement(veh) and (getElementType(veh) == "vehicle") then
		veh = getElementModel(veh)
	else
		veh = tonumber(veh)
	end
	for _, shop in ipairs(shopTable) do
		for _, car in ipairs(shop.cars) do
			if (car.model == veh) then
				return car.price, car.currency
			end
		end
	end
	return 0
end

function getShopTable()
	return shopTable
end
